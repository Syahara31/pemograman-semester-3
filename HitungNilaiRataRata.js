// Deklarasi Variabel dengan masing - masing nilai
let matematika = 85;
let BahasaInggris = 90;
let Ipa = 85;

// Perhitungan Rata -Rata
let NilaiRataRata = (matematika + BahasaInggris + Ipa) / 3;

// Tipe data dan output
console.log("Nilai matematika :" + matematika);
console.log("Nilai Bahasa inggris :" + BahasaInggris);
console.log("Nilai Ipa :" + Ipa);
console.log("Nilai Rata Rata :" + NilaiRataRata);

// Struktur Kondisi (if-else)
if (NilaiRataRata >=80) {
    console.log("Selamat Anda Lulus!")
} else {
    console.log("Maaf anda tidak Lulus")
}